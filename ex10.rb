# \t tabs in a string
tabby_cat = "\tI'm tabbed in."

# \n newline character
persian_cat = "I'm split\non a line."

# backslash escapes backslash
backslash_cat = "I'm \\ a \\ cat."

# triple quotes enable paragraph
# \t 	tabbed in
# \n\t 	newline also tabbed in
fat_cat = """
I\'ll do a list: tabbed in
\t* Cat food
\t* Fishies
\t* Catnip\n\t* Grass
"""

# demonstration putsing:
puts tabby_cat
puts persian_cat
puts backslash_cat
puts fat_cat